'''
    A soma simples basicamente ele vai perguntar os dois numeros em sequencia de cima pra baixo
    e armazenar o numero inteiro que voce escreveu dentro das variaveis numero1 e numero2
    depois outra variavel "soma" irá somar as duas variaveis "numero1" e "numero2"
    abaixo o print irá imprimir a mensagem com as 3 variaveis e o resultado da soma puxando ela
'''
numero1 = int(input('Primeiro numero: '))
numero2 = int(input('Segundo numero: '))

soma = numero1 + numero2
print(f'A soma entre {numero1} e {numero2} é: {soma}')