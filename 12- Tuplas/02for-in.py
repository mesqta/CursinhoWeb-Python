nomes = 'Alvaro', 'Vitor', 'Gustavo', 'Jose'

for nome in nomes:
    print(f'Nomes: {nome}')
print('Acabou')

'''
    No codigo acima, estamos fazendo um loop for que percorre a lista de nomes.  
    Em cada iteração do laço, o valor da variável "nome" é atribuido com o próximo elemento na lista "nomes".  
    A função print() imprime o que esta dentro das chaves, e f é uma string formatada .  
    O que está entre os parenteses é o que será impresso várias vezes. 
'''